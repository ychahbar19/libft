#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t		i;
	char		*d;

	i = 0;
	while (s[i] != '\0')
		i++;
	if (!(d = (char *)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (d);
}
